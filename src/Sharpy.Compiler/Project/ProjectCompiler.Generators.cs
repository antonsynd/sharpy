    /// <summary>
    /// Maximum size (in bytes) of source a single generator invocation may produce.
    /// Output that exceeds this limit is rejected with SPY0550 to prevent runaway
    /// generators from exhausting memory or compile time.
    /// </summary>
    private const int MaxGeneratedSourceBytes = 102_400;

    private GeneratorPartition PartitionGenerators()
    {
        _logger.LogInfo("Phase 4d: Partitioning source generators");

        var generatorFiles = new List<string>();
        var generatorTypes = new List<TypeSymbol>();

        foreach (var (filePath, unit) in _projectModel!.Units)
        {
            if (unit.Phase == CompilationPhase.Failed || unit.Ast == null)
                continue;

            foreach (var statement in unit.Ast.Body)
            {
                if (statement is ClassDef classDef)
                {
                    var symbol = SymbolTable.Lookup(classDef.Name) as TypeSymbol;
                    if (symbol is { IsSourceGenerator: true })
                    {
                        if (!generatorFiles.Contains(filePath))
                            generatorFiles.Add(filePath);
                        generatorTypes.Add(symbol);
                    }
                }
            }
        }

        if (generatorFiles.Count > 0)
        {
            _logger.LogInfo($"Found {generatorTypes.Count} source generator(s) in {generatorFiles.Count} file(s)");
        }

        return new GeneratorPartition(generatorFiles, generatorTypes);
    }

    private bool ExecuteGeneratorPipeline(
        GeneratorPartition partition,
        FileCompilationPipeline pipeline,
        ProjectConfig config,
        CancellationToken cancellationToken)
    {
        _logger.LogInfo("Phase 5b: Executing source generators");

        // Stage 1: Compile generator files to an in-memory assembly
        var generatorAssembly = CompileGeneratorAssembly(partition, config, cancellationToken);
        if (generatorAssembly == null)
            return false;

        // Stage 2: Execute generators against their target declarations
        var generatedSources = ExecuteGenerators(generatorAssembly, cancellationToken);
        if (generatedSources == null)
            return false;

        if (generatedSources.Count == 0)
        {
            _logger.LogInfo("No source was generated by any generator");
            return true;
        }

        // Stage 3: Re-parse generated source and merge into the compilation
        if (!IntegrateGeneratedSource(generatedSources, pipeline, config, cancellationToken))
            return false;

        _logger.LogInfo($"Source generator pipeline completed: {generatedSources.Count} generation(s) produced");
        return true;
    }

    private Assembly? CompileGeneratorAssembly(
        GeneratorPartition partition,
        ProjectConfig config,
        CancellationToken cancellationToken)
    {
        _logger.LogInfo("Stage 1: Compiling generator files to assembly");

        var generatorCSharp = new Dictionary<string, string>();
        var builtinRegistry = new BuiltinRegistry(_logger);

        foreach (var filePath in partition.GeneratorFiles)
        {
            var unit = _projectModel!.Units[filePath];
            if (unit.GeneratedCSharp != null)
            {
                generatorCSharp[filePath] = unit.GeneratedCSharp;
                continue;
            }

            var isEntryPoint = false;
            var isPackageInit = unit.ModulePath.EndsWith("__init__", StringComparison.Ordinal);

            var codeGenContext = new CodeGenContext(SymbolTable, builtinRegistry)
            {
                SourceFilePath = filePath,
                ProjectNamespace = config.RootNamespace,
                ProjectRootPath = ComputeSourceRootPath(config),
                IsEntryPoint = isEntryPoint,
                IsPackageInit = isPackageInit,
                Logger = _logger,
                SemanticBinding = _projectModel.SemanticBinding,
                SemanticInfo = SemanticInfo
            };

            var emitter = _emitterFactory.Create(codeGenContext, cancellationToken);
            var roslynUnit = emitter.GenerateCompilationUnit(unit.Ast!);
            var csharpCode = roslynUnit.ToFullString();

            if (codeGenContext.HasErrors)
            {
                _diagnostics.Merge(codeGenContext.Diagnostics);
                return null;
            }

            generatorCSharp[filePath] = csharpCode;
        }

        try
        {
            var syntaxTrees = generatorCSharp.Select(kvp =>
                CSharpSyntaxTree.ParseText(kvp.Value, path: kvp.Key,
                    encoding: System.Text.Encoding.UTF8)).ToList();

            var references = AssemblyCompiler.GetDefaultReferences();
            var compilation = CSharpCompilation.Create(
                config.RootNamespace + ".Generators",
                syntaxTrees,
                references,
                new CSharpCompilationOptions(OutputKind.DynamicallyLinkedLibrary)
                    .WithOptimizationLevel(OptimizationLevel.Debug));

            using var ms = new MemoryStream();
            var emitResult = compilation.Emit(ms);

            if (!emitResult.Success)
            {
                var errors = emitResult.Diagnostics
                    .Where(d => d.Severity == DiagnosticSeverity.Error)
                    .Select(d => d.GetMessage(System.Globalization.CultureInfo.InvariantCulture))
                    .Take(3);
                _diagnostics.AddError(
                    $"Failed to compile source generator assembly: {string.Join("; ", errors)}",
                    code: DiagnosticCodes.CodeGen.GeneratorExecutionError);
                return null;
            }

            ms.Seek(0, SeekOrigin.Begin);
            var loadContext = new AssemblyLoadContext("SharplyGenerators", isCollectible: true);
            var assembly = loadContext.LoadFromStream(ms);

            _logger.LogInfo($"Generator assembly compiled: {assembly.GetName().Name}");
            return assembly;
        }
        catch (Exception ex)
        {
            _diagnostics.AddError(
                $"Source generator assembly compilation failed: {ex.Message}",
                code: DiagnosticCodes.CodeGen.GeneratorExecutionError);
            return null;
        }
    }

    private List<GeneratedSource>? ExecuteGenerators(
        Assembly generatorAssembly,
        CancellationToken cancellationToken)
    {
        _logger.LogInfo("Stage 2: Executing generators against target declarations");

        var results = new List<GeneratedSource>();
        var contextBuilder = new GeneratorContextBuilder(SemanticInfo, _projectModel!.SemanticBinding, _logger);

        // Defense-in-depth: track which (generator type, target declaration) pairs we've
        // already executed during this pipeline run. The semantic phase normally prevents
        // duplicate bindings, but if one slips through we want to detect it instead of
        // silently producing duplicate output.
        var executedPairs = new HashSet<string>(StringComparer.Ordinal);

        foreach (var (declaration, bindings) in SemanticInfo.GetAllGeneratorBindings())
        {
            foreach (var binding in bindings)
            {
                cancellationToken.ThrowIfCancellationRequested();

                var generatorTypeName = binding.GeneratorType.Name;
                _logger.LogDebug($"Executing generator '{generatorTypeName}'");

                var pairKey = generatorTypeName + "::" + RuntimeHelpers.GetHashCode(declaration).ToString(System.Globalization.CultureInfo.InvariantCulture);
                if (!executedPairs.Add(pairKey))
                {
                    _logger.LogWarning(
                        $"Source generator '{generatorTypeName}' was invoked more than once for the same target; skipping duplicate invocation",
                        binding.Trigger.LineStart, binding.Trigger.ColumnStart);
                    continue;
                }

                var targetFilePath = FindFileForDeclaration(declaration);
                var generatorFilePath = binding.GeneratorType.DefiningFilePath;
                var argumentsHash = ComputeDecoratorArgumentsHash(binding.Trigger);
                var generatorIdentity = generatorTypeName + "@" + GetDeclarationName(declaration);

                // Record target -> generator dependency so a generator file edit
                // invalidates every target it generates code for.
                if (targetFilePath is not null && generatorFilePath is not null)
                {
                    var key = PathNormalizer.Normalize(targetFilePath);
                    if (!_generatorDependencies.TryGetValue(key, out var deps))
                    {
                        deps = new HashSet<string>(StringComparer.OrdinalIgnoreCase);
                        _generatorDependencies[key] = deps;
                    }
                    deps.Add(PathNormalizer.Normalize(generatorFilePath));
                }

                // Cache hit: reuse the previously-generated Sharpy source if nothing
                // it depends on has changed.
                if (_incrementalCache is not null
                    && targetFilePath is not null
                    && generatorFilePath is not null
                    && _incrementalCache.IsGeneratorCacheValid(
                        targetFilePath, generatorIdentity, generatorFilePath, argumentsHash, out var cachedSource)
                    && cachedSource is not null)
                {
                    _logger.LogDebug($"Generator '{generatorTypeName}' cache hit for {generatorIdentity}");
                    results.Add(new GeneratedSource(
                        Source: cachedSource,
                        GeneratorName: generatorTypeName,
                        TargetName: GetDeclarationName(declaration),
                        TriggerDecorator: binding.Trigger,
                        TargetDeclaration: declaration));
                    continue;
                }

                var genType = generatorAssembly.GetTypes()
                    .FirstOrDefault(t => t.Name == generatorTypeName
                        || t.Name == Shared.NameMangler.ToPascalCase(generatorTypeName));

                if (genType == null)
                {
                    _diagnostics.AddError(
                        $"Source generator type '{generatorTypeName}' not found in compiled generator assembly",
                        binding.Trigger.LineStart, binding.Trigger.ColumnStart,
                        code: DiagnosticCodes.CodeGen.GeneratorExecutionError);
                    continue;
                }

                try
                {
                    var generator = Activator.CreateInstance(genType)
                        as SharpyRT::Sharpy.Generators.SourceGenerator;

                    if (generator == null)
                    {
                        _diagnostics.AddError(
                            $"Failed to instantiate source generator '{generatorTypeName}'",
                            binding.Trigger.LineStart, binding.Trigger.ColumnStart,
                            code: DiagnosticCodes.CodeGen.GeneratorExecutionError);
                        continue;
                    }

                    var context = contextBuilder.Build(declaration, binding.Trigger, _projectModel.Config.RootNamespace);

                    // Execute with timeout
                    var task = Task.Run(() => generator.Generate(context), cancellationToken);
                    if (!task.Wait(TimeSpan.FromSeconds(30), cancellationToken))
                    {
                        _diagnostics.AddError(
                            $"Source generator '{generatorTypeName}' timed out after 30s",
                            binding.Trigger.LineStart, binding.Trigger.ColumnStart,
                            code: DiagnosticCodes.CodeGen.GeneratorTimeout);
                        continue;
                    }
                    var output = task.Result;

                    if (output?.Diagnostics != null)
                    {
                        foreach (var diag in output.Diagnostics)
                        {
                            if (diag.Severity == SharpyRT::Sharpy.Generators.GeneratorDiagnosticSeverity.Error)
                            {
                                _diagnostics.AddError(
                                    $"[{generatorTypeName}] {diag.Message}",
                                    binding.Trigger.LineStart, binding.Trigger.ColumnStart,
                                    code: DiagnosticCodes.CodeGen.GeneratorExecutionError);
                            }
                            else if (diag.Severity == SharpyRT::Sharpy.Generators.GeneratorDiagnosticSeverity.Warning)
                            {
                                _diagnostics.AddWarning(
                                    $"[{generatorTypeName}] {diag.Message}",
                                    binding.Trigger.LineStart, binding.Trigger.ColumnStart);
                            }
                        }
                    }

                    if (string.IsNullOrWhiteSpace(output?.Source))
                    {
                        _diagnostics.AddWarning(
                            $"Source generator '{generatorTypeName}' returned empty output",
                            binding.Trigger.LineStart, binding.Trigger.ColumnStart,
                            code: DiagnosticCodes.CodeGen.GeneratorEmptyOutput);
                        continue;
                    }

                    var sourceByteCount = System.Text.Encoding.UTF8.GetByteCount(output.Source);
                    if (sourceByteCount > MaxGeneratedSourceBytes)
                    {
                        _diagnostics.AddError(
                            $"Source generator '{generatorTypeName}' output exceeds {MaxGeneratedSourceBytes / 1024}KB limit (got {sourceByteCount} bytes)",
                            binding.Trigger.LineStart, binding.Trigger.ColumnStart,
                            code: DiagnosticCodes.CodeGen.GeneratorExecutionError);
                        continue;
                    }

                    var targetName = GetDeclarationName(declaration);

                    results.Add(new GeneratedSource(
                        Source: output.Source,
                        GeneratorName: generatorTypeName,
                        TargetName: targetName,
                        TriggerDecorator: binding.Trigger,
                        TargetDeclaration: declaration));

                    // Cache the successful generation so an unchanged subsequent
                    // build can skip the generator. We hash the generator source
                    // (so editing the generator invalidates) and the decorator
                    // arguments (so changing args invalidates).
                    if (_incrementalCache is not null
                        && targetFilePath is not null
                        && generatorFilePath is not null
                        && File.Exists(generatorFilePath))
                    {
                        var generatorHash = IncrementalCompilationCache.ComputeFileHash(generatorFilePath);
                        var targetHash = ComputeDeclarationHash(declaration);
                        _incrementalCache.CacheGeneratorOutput(
                            targetFilePath,
                            generatorIdentity,
                            generatorHash,
                            targetHash,
                            argumentsHash,
                            output.Source);
                    }
                }
                catch (TargetInvocationException tie) when (tie.InnerException != null)
                {
                    _diagnostics.AddError(
                        $"Source generator '{generatorTypeName}' failed: {tie.InnerException.Message}",
                        binding.Trigger.LineStart, binding.Trigger.ColumnStart,
                        code: DiagnosticCodes.CodeGen.GeneratorExecutionError);
                }
                catch (Exception ex) when (ex is not OperationCanceledException)
                {
                    _diagnostics.AddError(
                        $"Source generator '{generatorTypeName}' failed: {ex.Message}",
                        binding.Trigger.LineStart, binding.Trigger.ColumnStart,
                        code: DiagnosticCodes.CodeGen.GeneratorExecutionError);
                }
            }
        }

        return results;
    }

    private bool IntegrateGeneratedSource(
        List<GeneratedSource> generatedSources,
        FileCompilationPipeline pipeline,
        ProjectConfig config,
        CancellationToken cancellationToken)
    {
        _logger.LogInfo("Stage 3: Integrating generated source into compilation");

        foreach (var genSource in generatedSources)
        {
            cancellationToken.ThrowIfCancellationRequested();

            var syntheticPath = $"<generated:{genSource.GeneratorName}:{genSource.TargetName}>";
            _logger.LogDebug($"Parsing generated source: {syntheticPath}");

            // Lex and parse the generated source
            var sourceText = new Text.SourceText(genSource.Source, syntheticPath);
            var lexResult = FileCompilationPipeline.Lex(sourceText, _logger);
            if (lexResult.HasErrors)
            {
                _diagnostics.AddError(
                    $"Source generator '{genSource.GeneratorName}' produced source with lexer errors",
                    genSource.TriggerDecorator.LineStart, genSource.TriggerDecorator.ColumnStart,
                    code: DiagnosticCodes.CodeGen.GeneratorInvalidSource);
                continue;
            }

            var parseResult = FileCompilationPipeline.Parse(lexResult.Tokens, _logger);
            if (parseResult.HasErrors)
            {
                _diagnostics.AddError(
                    $"Source generator '{genSource.GeneratorName}' produced source with parse errors",
                    genSource.TriggerDecorator.LineStart, genSource.TriggerDecorator.ColumnStart,
                    code: DiagnosticCodes.CodeGen.GeneratorInvalidSource);
                continue;
            }

            var module = parseResult.Module;
            if (module == null)
                continue;

            // Find the target file and merge generated statements
            var targetFile = FindFileForDeclaration(genSource.TargetDeclaration);
            if (targetFile == null)
                continue;

            var targetUnit = _projectModel!.Units[targetFile];
            if (targetUnit.Ast == null)
                continue;

            // Add generated statements to the target module's AST, and record each
            // generated statement in SemanticInfo so LSP / tooling can attribute it
            // back to the generator (Phase 7 — "Generated by @[X]" on hover).
            var existingStatements = targetUnit.Ast.Body.ToList();
            foreach (var generatedStmt in module.Body)
            {
                SemanticInfo.MarkAsGenerated(generatedStmt, genSource.GeneratorName);
                existingStatements.Add(generatedStmt);
            }
            targetUnit.Ast = targetUnit.Ast with
            {
                Body = System.Collections.Immutable.ImmutableArray.CreateRange(existingStatements)
            };

            // Invalidate cached C# so codegen re-processes this file
            targetUnit.GeneratedCSharp = null;

            // Run name resolution on generated declarations
            var nameResolveResult = pipeline.ResolveNames(module, cancellationToken);
            foreach (var error in nameResolveResult.Diagnostics.GetErrors())
            {
                _diagnostics.AddError(
                    $"In generated code from @[{genSource.GeneratorName}]: {error.Message}",
                    genSource.TriggerDecorator.LineStart, genSource.TriggerDecorator.ColumnStart,
                    code: error.Code);
            }

            // Type check the generated code in context of the target file
            var typeCheckResult = pipeline.TypeCheck(
                module,
                targetFile,
                isEntryPoint: false,
                maxErrors: _maxErrors,
                existingDiagnostics: _diagnostics,
                cancellationToken: cancellationToken);

            foreach (var error in typeCheckResult.TypeChecker.Diagnostics.GetErrors())
            {
                _diagnostics.AddError(
                    $"In generated code from @[{genSource.GeneratorName}]: {error.Message}",
                    genSource.TriggerDecorator.LineStart, genSource.TriggerDecorator.ColumnStart,
                    code: error.Code);
            }

            _logger.LogInfo($"Integrated generated source from @[{genSource.GeneratorName}] on {genSource.TargetName}");
        }

        return !_diagnostics.HasErrors;
    }

    private string? FindFileForDeclaration(Statement declaration)
    {
        foreach (var (filePath, unit) in _projectModel!.Units)
        {
            if (unit.Ast == null)
                continue;

            foreach (var stmt in unit.Ast.Body)
            {
                if (ReferenceEquals(stmt, declaration))
                    return filePath;
            }
        }
        return null;
    }

    private static string GetDeclarationName(Statement declaration) => declaration switch
    {
        ClassDef cd => cd.Name,
        FunctionDef fd => fd.Name,
        StructDef sd => sd.Name,
        _ => "Unknown"
    };

    /// <summary>
    /// Hashes a decorator's arguments (positional + keyword) to detect when a generator's
    /// inputs change. Returns null when the decorator takes no arguments.
    /// </summary>
    private static string? ComputeDecoratorArgumentsHash(Decorator trigger)
    {
        if (trigger.Arguments.IsDefaultOrEmpty && trigger.KeywordArguments.IsDefaultOrEmpty)
        {
            return null;
        }

        var sb = new System.Text.StringBuilder();
        foreach (var arg in trigger.Arguments)
        {
            sb.Append(arg.ToString());
            sb.Append('|');
        }
        sb.Append("::");
        foreach (var kw in trigger.KeywordArguments)
        {
            sb.Append(kw.Name);
            sb.Append('=');
            sb.Append(kw.Value.ToString());
            sb.Append('|');
        }
        var hashBytes = System.Security.Cryptography.SHA256.HashData(
            System.Text.Encoding.UTF8.GetBytes(sb.ToString()));
        return Convert.ToHexStringLower(hashBytes);
    }

    /// <summary>
    /// Hashes a single declaration's AST string form to detect when a generator's
    /// target changes independently of unrelated changes in the same file.
    /// </summary>
    private static string ComputeDeclarationHash(Statement declaration)
    {
        var text = declaration.ToString() ?? string.Empty;
        var hashBytes = System.Security.Cryptography.SHA256.HashData(
            System.Text.Encoding.UTF8.GetBytes(text));
        return Convert.ToHexStringLower(hashBytes);
    }
}

internal sealed record GeneratorPartition(
    List<string> GeneratorFiles,
    List<TypeSymbol> GeneratorTypes)
{
    public bool HasGenerators => GeneratorTypes.Count > 0;
}

internal sealed record GeneratedSource(
    string Source,
    string GeneratorName,
    string TargetName,
    Decorator TriggerDecorator,
    Statement TargetDeclaration);
